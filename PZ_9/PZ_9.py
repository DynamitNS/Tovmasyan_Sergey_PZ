# Используя словарь посчитать количество уникальных слов в заданном
# предложении «Изучаем язык Питон». Вывести на экран каждую пару
# «ключ:значение».
# Заданное предложение
sentence = "Изучаем язык Питон"

# Разбиваем предложение на слова
words = sentence.split()

# Создаем пустой словарь для подсчета уникальных слов
word_count = {}

# Подсчитываем количество каждого слова
for word in words:
    if word in word_count:
        word_count[word] += 1
    else:
        word_count[word] = 1

# Выводим каждую пару "ключ:значение"
for key, value in word_count.items():
    print(f"{key}:{value}")

# Выводим количество уникальных слов
print(f"Количество уникальных слов: {len(word_count)}")
